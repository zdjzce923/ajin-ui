<template>
  <div class="g-button-group">
    <slot />
  </div>
</template>

<script>
export default {
  mounted() {
    for (let node of this.$el.children) {
      let name = node.nodeName.toLowerCase();
      if (name !== "button") {
        console.error(`g-button-group 的子元素应是 g-button 而你写的是${name}`);
      }
    }
  },
};
</script>

